// using Array Literal
let persons: string[] = ['Ajay', 'Roshan', 'Rohit', 'Sanjay'];
console.log(persons);

// using Array Class
let marks: Array<number> = [55,65,75,85,95];
console.log(marks);

// Multi-Dimensional Array
let matrix: number[][] = [
    [1,2,3],
    [4,5,6],
    [7,8,9]
]
console.log(matrix);

// Array of Mixed Type
let anyArray: [number, string] = [1, 'Ajay']
console.log(anyArray);
